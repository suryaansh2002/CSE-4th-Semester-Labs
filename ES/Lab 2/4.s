	AREA RESET,  DATA, READONLY
	EXPORT __Vectors
__Vectors
		DCD 0X10001000
		DCD Reset_Handler
		ALIGN
		AREA mycode, CODE, READONLY 
		ENTRY
		EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC1
	LDR R1,=SRC2
	
	MOV R3, #4
	
	LDR R2,=DST
	MOV R6, #0
	
LOOP
	LDR R4, [R0],#4
	LDR R5, [R1], #4
	SUBS R7, R4, R5
	ADDS R7, R7, R6
	MOV R6, #0
	SBC R6, R6, #0
	STR R7, [R2], #4
	SUBS R3, #1
	BNE LOOP
	STR R6,[R2]
STOP
	B STOP
	
SRC1 DCD 0X0FFF0FFAF, 0X0FFF0FFBF, 0X0FFF0FCF, 0X0FFF0FDF
SRC2 DCD 0X0FFF00FF, 0X0FFF0F0F, 0X0FFFAFFF, 0X0FFF0FEF
	AREA mydata, DATA, READWRITE
DST DCD 0
	END