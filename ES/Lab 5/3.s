	AREA MYCODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =SRC
	MOV R4, #10
	MOV R6,#0
	MOV R3, #0X27 ; ELEMENT TO SEARCH FOR

UP	TEQ R4, #0
	BEQ STOP
	LDR R1, [R0], #4
	TEQ R1, R3
	BEQ FOUND
	SUB R4, #1
	add R6,#1
	B UP

FOUND MOV R5,#1

STOP B STOP


SRC DCD 0x30,0x29,0x28,0x27,0x26,0x25,0x24,0x23,0x22,0x21

	AREA mydata,DATA,READWRITE

	END