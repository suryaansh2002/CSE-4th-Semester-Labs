	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=NUM1
	LDR R1, =NUM2
	LDR R2,[R0]
	LDR R3,[R1]
LOOP CMP R2,R3
	 BCC SUB2
	 SUB R2,R3
	 B CHECK
SUB2 SUB R3,R2
	B CHECK

CHECK
	TEQ R2,R3
	BNE LOOP
	LDR R6,=ANS
	LDR R4,[R0]
	LDR R5,[R1]
	MUL R3,R4,R5
	STR R2,[R6],#4
	MOV R7, #0
UP CMP R3,R2
	BCC STORE
	SUB R3,R2
	ADD R7,#1
	B UP
STORE STR R7,[R6]

STOP
	B STOP
NUM1 DCD 12
NUM2 DCD 16
	AREA mydata,DATA,READWRITE
ANS DCD 0
	END
